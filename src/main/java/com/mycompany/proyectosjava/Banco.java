    private void btnConfirmarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarRegistroActionPerformed
        try {

            String nombre = txtRegUsuario.getText().trim();
            String pass = new String(txtRegPass.getPassword());
            String montoTexto = txtRegMonto.getText().trim();

            if (nombre.isEmpty() || pass.isEmpty() || montoTexto.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
                return;
            }

            for (Usuario u : listaUsuarios) {
                if (u.getId().equalsIgnoreCase(nombre)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "El nombre de usuario ya existe.");
                    return;
                }
            }

            double saldoInicial = Double.parseDouble(montoTexto);
            if (saldoInicial < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "El saldo no puede ser negativo.");
                return;
            }

            listaUsuarios.add(new Usuario(nombre, pass, saldoInicial));
            javax.swing.JOptionPane.showMessageDialog(this, "¡Usuario registrado con éxito!");

            limpiarCamposRegistro();
            CardLayout cl = (CardLayout) panelCards.getLayout();
            cl.show(panelCards, "card2");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: El monto debe ser un número válido.");
        }
    }//GEN-LAST:event_btnConfirmarRegistroActionPerformed

