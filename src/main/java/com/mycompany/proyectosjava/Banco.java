    private void setImagenLabel(String ruta, javax.swing.JLabel etiqueta) {
        try {
            java.net.URL imgURL = getClass().getResource(ruta);

            if (imgURL != null) {
                javax.swing.ImageIcon iconoOriginal = new javax.swing.ImageIcon(imgURL);
                java.awt.Image imagen = iconoOriginal.getImage();

                int width = etiqueta.getWidth() > 0 ? etiqueta.getWidth() : 1222;
                int height = etiqueta.getHeight() > 0 ? etiqueta.getHeight() : 774;

                java.awt.Image imagenEscalada = imagen.getScaledInstance(width, height, java.awt.Image.SCALE_SMOOTH);
                etiqueta.setIcon(new javax.swing.ImageIcon(imagenEscalada));
            } else {
                System.err.println("No se encontró la imagen en: " + ruta);
            }
        } catch (Exception e) {
            System.err.println("Error cargando imagen: " + e.getMessage());
        }
    }

