    private void btnConfirmarTransferenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarTransferenciaActionPerformed
        try {
            String idDestino = txtDestinatario.getText().trim();
            double monto = Double.parseDouble(txtMontoTransferir.getText());
            Usuario receptor = null;

            for (Usuario u : listaUsuarios) {
                if (u.getId().equals(idDestino)) {
                    receptor = u;
                    break;
                }
            }

            if (receptor == null) {
                javax.swing.JOptionPane.showMessageDialog(this, "El destinatario no existe.");
                return;
            }
            if (idDestino.equals(usuarioActual.getId())) {
                javax.swing.JOptionPane.showMessageDialog(this, "No puedes transferirte a ti mismo.");
                return;
            }
            if (usuarioActual.getSaldo() < monto) {
                javax.swing.JOptionPane.showMessageDialog(this, "Saldo insuficiente.");
                return;
            }

            usuarioActual.setSaldo(usuarioActual.getSaldo() - monto);
            receptor.setSaldo(receptor.getSaldo() + monto);

            javax.swing.JOptionPane.showMessageDialog(this, "Transferencia exitosa por $" + monto);

            txtDestinatario.setText("");
            txtMontoTransferir.setText("");
            CardLayout cl = (CardLayout) panelCards.getLayout();
            cl.show(panelCards, "card4");

        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Ingresa un monto válido.");
        }
    }//GEN-LAST:event_btnConfirmarTransferenciaActionPerformed

